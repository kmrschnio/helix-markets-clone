// Generate random sparkline data
function generateSparkline(trend: "up" | "down" | "flat", length = 30): number[] {
  const data: number[] = [];
  let value = 50 + Math.random() * 20;
  for (let i = 0; i < length; i++) {
    const direction = trend === "up" ? 0.6 : trend === "down" ? 0.4 : 0.5;
    value += (Math.random() - (1 - direction)) * 5;
    value = Math.max(10, Math.min(90, value));
    data.push(value);
  }
  return data;
}

